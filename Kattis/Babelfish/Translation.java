public class Translation {
    private String e;
    private String g;
    public Translation(String e, String g) {
        this.e = e;
        this.g = g;
    }
    public Translation getTranslation() {
        return this;
    }
    public String getE() {
        return this.e;
    }
    public String getG() {
        return this.g;
    }
}
